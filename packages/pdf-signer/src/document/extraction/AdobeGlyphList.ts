/**
 * Look up a glyph name and return the Unicode code point, or undefined.
 */
export function glyphNameToUnicode(name: string): number | undefined {
  // Direct lookup
  const direct = GLYPH_LIST[name];
  if (direct !== undefined) return direct;

  // Try "uniXXXX" convention
  if (name.startsWith('uni') && name.length === 7) {
    const cp = parseInt(name.substring(3), 16);
    if (!isNaN(cp)) return cp;
  }

  // Try "uXXXXX" convention (for code points > 0xFFFF)
  if (name.startsWith('u') && name.length >= 5 && name.length <= 7) {
    const hex = name.substring(1);
    if (/^[0-9A-Fa-f]+$/.test(hex)) {
      const cp = parseInt(hex, 16);
      if (!isNaN(cp)) return cp;
    }
  }

  return undefined;
}
