/**
 * Font encoding tables for PDF standard fonts.
 * WinAnsi (Windows-1252), Symbol, and ZapfDingbats encodings.
 *
 * Maps Unicode code points → { code (byte in font), name (glyph name) }.
 * Data extracted from @pdf-lib/standard-fonts.
 */

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

export interface EncodedGlyph {
  code: number;
  name: string;
}

export interface FontEncoding {
  readonly name: string;
  canEncode(codePoint: number): boolean;
  encode(codePoint: number): EncodedGlyph;
}

// ---------------------------------------------------------------------------
// Internal: encoding from a Map
// ---------------------------------------------------------------------------

class MapEncoding implements FontEncoding {
  readonly name: string;
  private readonly map: ReadonlyMap<number, EncodedGlyph>;

  constructor(name: string, map: ReadonlyMap<number, EncodedGlyph>) {
    this.name = name;
    this.map = map;
  }

  canEncode(codePoint: number): boolean {
    return this.map.has(codePoint);
  }

  encode(codePoint: number): EncodedGlyph {
    const mapped = this.map.get(codePoint);
    if (!mapped) {
      const str = String.fromCodePoint(codePoint);
      const hex = '0x' + codePoint.toString(16).padStart(4, '0');
      throw new Error(`${this.name} cannot encode "${str}" (${hex})`);
    }
    return mapped;
  }
}

