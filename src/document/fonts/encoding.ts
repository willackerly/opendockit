export const WinAnsiEncoding: FontEncoding = new MapEncoding('WinAnsi', winAnsiMap);
export const SymbolEncoding: FontEncoding = new MapEncoding('Symbol', symbolMap);
export const ZapfDingbatsEncoding: FontEncoding = new MapEncoding('ZapfDingbats', zapfDingbatsMap);

/**
 * Get the appropriate encoding for a standard font name.
 */
export function encodingForFont(fontName: string): FontEncoding {
  if (fontName === 'Symbol') return SymbolEncoding;
  if (fontName === 'ZapfDingbats') return ZapfDingbatsEncoding;
  return WinAnsiEncoding;
}

/**
 * Encode a Unicode text string to hex using the given encoding.
 * Each character → 2 hex digits (byte code in the font's encoding).
 * Matches pdf-lib's `StandardFontEmbedder.encodeText()`.
 */
export function encodeTextToHex(text: string, encoding: FontEncoding): string {
  const codePoints = Array.from(text);
  const hexCodes: string[] = new Array(codePoints.length);
  for (let i = 0; i < codePoints.length; i++) {
    const cp = codePoints[i].codePointAt(0)!;
    const glyph = encoding.encode(cp);
    hexCodes[i] = glyph.code.toString(16).toUpperCase().padStart(2, '0');
  }
  return hexCodes.join('');
}
